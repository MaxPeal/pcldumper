    @Override
    final String execute(final PrinterCommand command, final PclDumperContext context) {
        final ParameterizedPclCommand cmd = (ParameterizedPclCommand) command;
        final int value = Integer.parseInt(cmd.getValue());
        final int vendorValue = getVendorValue(value);
        final int typefaceValue = value - vendorValue;

        final String vendor = VENDOR_MAP.get(Integer.valueOf(vendorValue));
        final String typeface = TYPEFACE_MAP.get(Integer.valueOf(typefaceValue));

        final StringBuilder sb = new StringBuilder();
        if (typeface != null) {
            sb.append(typeface);
        } else {
            sb.append("Unknown typeface");
        }

        sb.append(" from ");

        if (vendor != null) {
            sb.append(vendor);
        } else {
            sb.append("an unknown vendor");
        }

        return sb.toString();
    }

    private static int getVendorValue(final int value) {
        if (value >= BIGELOW) {
            return BIGELOW;
        } else if (value >= ADOBE) {
            return ADOBE;
        } else if (value >= MONOTYPE) {
            return MONOTYPE;
        } else if (value >= LINOTYPE) {
            return LINOTYPE;
        } else if (value >= BITSTREAM) {
            return BITSTREAM;
        } else if (value >= AGFA) {
            return AGFA;
        } else {
            return 0; // This should really never happen...
        }
    }
